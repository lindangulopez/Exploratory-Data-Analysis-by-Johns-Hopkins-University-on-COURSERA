## ------------------------------------------------------------------------
sumsq <- function(mu, sigma, x) {
        sum(((x - mu) / sigma)^2)
}

## ------------------------------------------------------------------------
x <- rnorm(100)       ## Generate some data
sumsq(1:10, 1:10, x)  ## This is not what we want

## ------------------------------------------------------------------------
mapply(sumsq, 1:10, 1:10, MoreArgs = list(x = x))

## ------------------------------------------------------------------------
vsumsq <- Vectorize(sumsq, c("mu", "sigma"))
vsumsq(1:10, 1:10, x)

